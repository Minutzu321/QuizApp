package me.minutz.trivsrv.useri;

public class Clasa {

	private int procent;
	private String tip;
	
	public Clasa(int procent, String tip) {
		this.procent = procent;
		this.tip = tip;
	}
	public int getProcent() {
		return procent;
	}
	public void setProcent(int procent) {
		this.procent = procent;
	}
	public String getTip() {
		return tip;
	}
	public void setTip(String tip) {
		this.tip = tip;
	}
	
}
